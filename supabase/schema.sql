-- ======================= INDEXES FOR PERFORMANCE =======================
create index idx_users_auth_uid on public.users(auth_uid);
create index idx_chats_user_id on public.chats(user_id);
create index idx_messages_chat_id on public.messages(chat_id);
create index idx_saved_items_user_id on public.saved_items(user_id);

-- ======================= HELPER FUNCTION TO CREATE PROFILE AFTER SIGNUP =======================
-- This function automatically creates a user profile when a new auth user signs up
create or replace function public.handle_new_user()
returns trigger as $$
begin
  insert into public.users (auth_uid, name, email, created_at)
  values (new.id, new.raw_user_meta_data->>'name', new.email, now());
  return new;
end;
$$ language plpgsql security definer;

-- Trigger to call the function on auth user creation
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

-- ======================= GRANT PERMISSIONS =======================
-- Allow authenticated users to use the functions
grant usage on schema public to authenticated;
grant all on public.users to authenticated;
grant all on public.chats to authenticated;
grant all on public.messages to authenticated;
grant all on public.saved_items to authenticated;
